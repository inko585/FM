        public FileSelectionForm(string description, string title, string fileType)
        {

            InitializeComponent();
            this.label1.Text = description;
            this.Text = title;
            this.TopMost = true;
            ofd = new OpenFileDialog();
            ofd.Filter = "HCV"+ "| *" + fileType;
            ofd.RestoreDirectory = true;
        }

        public string Result
        {
            get
